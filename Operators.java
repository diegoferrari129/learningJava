// % da il resto della divisione
public class Operators {
    public static void main(String[] args) {
        // % da il resto della divisione
        int a = 10;
        int b = 3;
        int c = a % b;
        System.out.println(c);

        // shorthand operator
        int d = 10;
        d += 3;
        System.out.println(d);
    }
}
